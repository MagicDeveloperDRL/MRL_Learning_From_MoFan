import tensorflow as tf
import numpy as np
GLOBAL_NET_SCOPE = 'Global_Net'

# Actor-Critic 网络
class ACNet(object):
    def __init__(self, n_features,n_actions, a_bound,scope, globalAC=None):
        self.n_features = n_features
        self.n_actions = n_actions
        self.a_bound = a_bound

        if scope == GLOBAL_NET_SCOPE:  # 创建全局网络（global net）
            with tf.variable_scope(scope):
                self.s = tf.placeholder(tf.float32, [None, self.n_features], 'S') # 定义网络输入
                self.a_params, self.c_params = self._build_net(scope)[-2:] # 获取其全局网络的actor和critic的参数
        else:   # 创建局部网络（local net）并计算损失
            with tf.variable_scope(scope):
                self.s = tf.placeholder(tf.float32, [None, self.n_features], 'S')
                self.a_his = tf.placeholder(tf.float32, [None, self.n_actions], 'A')
                self.v_target = tf.placeholder(tf.float32, [None, 1], 'Vtarget')

                mu, sigma, self.v, self.a_params, self.c_params = self._build_net(scope)

                td = tf.subtract(self.v_target, self.v, name='TD_error')
                with tf.name_scope('c_loss'):
                    self.c_loss = tf.reduce_mean(tf.square(td))

                with tf.name_scope('wrap_a_out'):
                    mu, sigma = mu * a_bound[1], sigma + 1e-4

                normal_dist = tf.distributions.Normal(mu, sigma)

                with tf.name_scope('a_loss'):
                    log_prob = normal_dist.log_prob(self.a_his)
                    exp_v = log_prob * tf.stop_gradient(td)
                    entropy = normal_dist.entropy()  # encourage exploration
                    self.exp_v = ENTROPY_BETA * entropy + exp_v
                    self.a_loss = tf.reduce_mean(-self.exp_v)

                with tf.name_scope('choose_a'):  # use local params to choose action
                    self.A = tf.clip_by_value(tf.squeeze(normal_dist.sample(1), axis=[0, 1]), self.a_bound[0], self.a_bound[1])
                with tf.name_scope('local_grad'):
                    self.a_grads = tf.gradients(self.a_loss, self.a_params)
                    self.c_grads = tf.gradients(self.c_loss, self.c_params)

            with tf.name_scope('sync'):
                with tf.name_scope('pull'):
                    self.pull_a_params_op = [l_p.assign(g_p) for l_p, g_p in zip(self.a_params, globalAC.a_params)]
                    self.pull_c_params_op = [l_p.assign(g_p) for l_p, g_p in zip(self.c_params, globalAC.c_params)]
                with tf.name_scope('push'):
                    self.update_a_op = OPT_A.apply_gradients(zip(self.a_grads, globalAC.a_params))
                    self.update_c_op = OPT_C.apply_gradients(zip(self.c_grads, globalAC.c_params))

    def _build_net(self, scope):
        w_init = tf.random_normal_initializer(0., .1)
        with tf.variable_scope('actor'):
            l_a = tf.layers.dense(self.s, 200, tf.nn.relu6, kernel_initializer=w_init, name='la') # 输入层
            mu = tf.layers.dense(l_a, self.n_actions, tf.nn.tanh, kernel_initializer=w_init, name='mu') # 均值输出层
            sigma = tf.layers.dense(l_a, self.n_actions, tf.nn.softplus, kernel_initializer=w_init, name='sigma') # 标准差输出层
        with tf.variable_scope('critic'):
            l_c = tf.layers.dense(self.s, 100, tf.nn.relu6, kernel_initializer=w_init, name='lc') # 输入层
            v = tf.layers.dense(l_c, 1, kernel_initializer=w_init, name='v')  # 状态价值输出层
        a_params = tf.get_collection(tf.GraphKeys.TRAINABLE_VARIABLES, scope=scope + '/actor') # 获取指定actor模型的参数
        c_params = tf.get_collection(tf.GraphKeys.TRAINABLE_VARIABLES, scope=scope + '/critic') # 虎丘指定critic模型的参数
        return mu, sigma, v, a_params, c_params

    def update_global(self, feed_dict):  # run by a local
        SESS.run([self.update_a_op, self.update_c_op], feed_dict)  # local grads applies to global net

    def pull_global(self):  # run by a local
        SESS.run([self.pull_a_params_op, self.pull_c_params_op])

    def choose_action(self, s):  # run by a local
        s = s[np.newaxis, :]
        return SESS.run(self.A, {self.s: s})

